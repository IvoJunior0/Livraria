public class Livro {
    private String titulo;
    private String autor;
    private String edicao;
    private int ano;
    private int numeroPaginas;

    public Livro(String titulo, String autor, String edicao, int ano, int numeroPaginas) {
        this.titulo = titulo;
        this.autor = autor;
        this.edicao = edicao;
        this.ano = ano;
        this.numeroPaginas = numeroPaginas;
    }
}
